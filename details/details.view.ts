namespace $.$mol {

export class $my_toys_details extends  $.$my_toys_details {

		toy_image() {
			return this.toy().image()
		}

		toy_title() {
			return this.toy().title()
		}

		toy_type() {
			return this.toy().type()
		}

		toy_price() {
			return this.toy().price()
		}

		toy_size() {
			return this.toy().size()
		}

		toy_count() {
			return this.toy().count()
		}

	}

}
