namespace $.$$ {

export class $hyoo_toys_details extends  $.$hyoo_toys_details {

		photo_uri() {
			return this.toy().image()
		}

		title() {
			return this.toy().title()
		}

		type() {
			return this.toy().type()
		}

		price() {
			return this.toy().price()
		}

		size() {
			return this.toy().size()
		}

		reviews() {
			return this.toy().reviews()
		}

	}

}
